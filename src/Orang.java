
public class Orang {
	private String nama;
	private byte umur;
	
	public Orang(String nama,byte umur) {
		this.nama = nama;
		this.umur = umur;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public byte getUmur() {
		return umur;
	}
	public void setUmur(byte umur) {
		this.umur = umur;
	}
}
